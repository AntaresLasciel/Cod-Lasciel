    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        int cantDias = Integer.parseInt(String.valueOf(spnDias.getValue()));
        int totalHoras = cantDias * 24;
         int totalMinutos = totalHoras * 60;
         int totalSegundos = totalMinutos * 60;
         String totalHorastxt = String.valueOf(totalHoras);
         String totalMinutostxt = String.valueOf(totalMinutos);
         String totalSegundostxt = String.valueOf(totalSegundos);
         String totalDiastxt = String.valueOf(spnDias.getValue());
         lblTotalHoras.setText(totalHorastxt);
         lblTotalMinutos.setText(totalMinutostxt);
         lblTotalSegundos.setText(totalSegundostxt);
         
         lblResultadoAviso.setText(totalDiastxt + " Dias tiene " + totalHorastxt + " Horas - " + totalMinutostxt + " Minutos - " + totalSegundostxt + " Segundos " );
         
         
         
         
    }//GEN-LAST:event_btnCalcularActionPerformed

